private boolean existeCamposObligatoriosDeAgregarClienteEnBlanco() {
        boolean existe = false;
        if (this.txtcedulaactualizar.getText().length() == 0) {
            this.jlblErrorAgregarCI.setVisible(true);
            existe = true;
        }
        if (this.txtnombreactualizar.getText().length() == 0) {
            this.jlblErrorAgregarNombres.setVisible(true);
            existe = true;
        }
        if (this.txtapellidoactualizar.getText().length() == 0) {
            this.jlblErrorAgregarApellido.setVisible(true);
            existe = true;
        }
        return existe;
    }
private boolean existeCedulaCliente() {
        boolean existe = false;
        if (bd.existeCedulaCliente(this.txtcedulaactualizar.getText())) {
            existe = true;
        }
        return existe;
    }
