private void cargarPelicula(){
    String Pelicula= this.txtPeliculaTitulo.getText();
        if (existeTituloPelicula(Pelicula)){
            listaEjemplaresPorPelicula = ListarEjemplares();
            int stockDVD = 0;
            int stockBluRay = 0;
            txtPeliculaGenero.setText(ejemplar.getGenero());
            this.txtPeliculaTitulo.setText(Pelicula);
           InputStream imagen= ejemplar.getFoto();    
             for (int i = 0; i < this.listaEjemplaresPorPelicula.size(); i++) {
                
                this.jtblPeliculaEjemplares.setValueAt((i + 1), i, 0);
                this.jtblPeliculaEjemplares.setValueAt(this.listaEjemplaresPorPelicula.get(i).getidEjemplar(), i, 1);
                this.jtblPeliculaEjemplares.setValueAt(this.listaEjemplaresPorPelicula.get(i).getFormato(), i, 2);
                this.jtblPeliculaEjemplares.setValueAt(this.listaEjemplaresPorPelicula.get(i).getPrecioAlquiler(), i, 3);
                this.jtblPeliculaEjemplares.setValueAt(this.listaEjemplaresPorPelicula.get(i).getPrecioVenta(), i,4);
                this.jtblPeliculaEjemplares.setValueAt(this.listaEjemplaresPorPelicula.get(i).getEstado(), i, 5);
                
                if (listaEjemplaresPorPelicula.get(i).getFormato().equalsIgnoreCase("DVD")) {
                    stockDVD++;
                }
                
                if (listaEjemplaresPorPelicula.get(i).getFormato().equalsIgnoreCase("BLU-RAY")) {
                    stockBluRay++;
                }
                
                
            }
             if (imagen!=null){
        try {
           Image icono = ImageIO.read(imagen).getScaledInstance(lblImagenPelicula.getWidth(),
                    lblImagenPelicula.getHeight(), Image.SCALE_DEFAULT);
                   lblImagenPelicula.setIcon(new ImageIcon(icono));
                   lblImagenPelicula.updateUI();
        } catch (IOException ex) {
            Logger.getLogger(ActualizarPelicula.class.getName()).log(Level.SEVERE, null, ex);
        }
        
               
    }else {
        this.lblImagenPelicula.setIcon(new javax.swing.ImageIcon(getClass().getResource("/recursos/Imagenes/incognito.jpg")));
    }
            this.txtPeliculaStock.setText("" + (stockDVD + stockBluRay));
            this.txtPeliculaStockDVD.setText("" + stockDVD);
            this.txtPeliculaStockBluRay.setText("" + stockBluRay);
        } else {
            JOptionPane.showMessageDialog(null, "La Pelicula :" + Pelicula + " no existe!");
        
            
            
        
            
            
            
       }
}

public ArrayList<Ejemplar> ListarEjemplares(){
    ListaEjemplar.clear();
    int i=0;
        ConeccionBD z = new ConeccionBD();
            Connection cn = z.conectar();
            String sql = "SELECT *FROM vejemplar where Titulo= '"+txtPeliculaTitulo.getText()+"' "
                    + "and Estado= 'LIBRE' ";
            try {
                Statement st= cn.createStatement();
                rs= st.executeQuery(sql);
                while(rs.next()){
                    int idEjemplar= rs.getInt("Codigo");
                    int codiPeli= rs.getInt("CodigoPelicula");
                    String peli= rs.getString("Titulo");
                    String Gene= rs.getString("Genero");
                    String formato = rs.getString("Formato");
                    int PrecioAlquiler= rs.getInt("PrecioAlquiler");
                    int PrecioVenta= rs.getInt("PrecioVenta");
                    String stock= rs.getString("Stock");
                    String Estado= rs.getString("Estado");
                    InputStream foto= rs.getBinaryStream("Foto");
                    ejemplar = new Ejemplar(idEjemplar,codiPeli, peli, Gene, formato, PrecioAlquiler,stock, PrecioVenta, Estado, foto);
                    ListaEjemplar.add(i, ejemplar);
                    i++;
                }
    } catch (Exception e) {
         Logger.getLogger(ConeccionBD.class.getName()).log(Level.SEVERE, null, e);
    }
            return ListaEjemplar;
}
