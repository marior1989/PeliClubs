private void limpiar(){
        txtAgregarStockCantidadAAgregar.setText("");
        txtPeliculaGenero.setText("");
        txtPeliculaStockBluRay.setText("");
        txtPeliculaTitulo.setText("");
        txtPeliculaStock.setText("");
        txtPeliculaStockDVD.setText("");
        cmbAgregarStockFormato.setSelectedItem(null);
        this.listaEjemplaresPorPelicula.clear();
        
    }
    private void limpiartabla(){
        for (int e = 0; e < this.jtblPeliculaEjemplares.getRowCount(); e++) {
            this.jtblPeliculaEjemplares.setValueAt("", e, 0);
            this.jtblPeliculaEjemplares.setValueAt("", e, 1);
            this.jtblPeliculaEjemplares.setValueAt("", e, 2);
            this.jtblPeliculaEjemplares.setValueAt("", e, 3);
            this.jtblPeliculaEjemplares.setValueAt("", e, 4);
            this.jtblPeliculaEjemplares.setValueAt("", e, 5);
            
        }
    }
    private void AgregarEjemplar(){
        int cantidadAgregar = Integer.parseInt(this.txtAgregarStockCantidadAAgregar.getText());
                int idpelicula= this.ejemplar.getCodigoPelicula();
                String Formato= this.cmbAgregarStockFormato.getSelectedItem().toString().trim().toUpperCase();
                int stock= Integer.parseInt(this.txtPeliculaStock.getText().toString());
            
               int opcion= bd.GrabarEjemplar(idpelicula, Formato, stock, cantidadAgregar);
               
               if (opcion !=0){
                   JOptionPane.showMessageDialog(rootPane, "Se ha agregado correctamente");
                   limpiar();
                   limpiartabla();
               }else {
                   JOptionPane.showMessageDialog(rootPane, "Error al cargar");
               }
    }

